num=int(input("Enter a number : "))
i=1#Start(zero is multiply by aero)
while i<=10:
    print(num,"*",i,"=",num*i)#input number, multiplcation symbol,i value,multiply
    i += 1#assignment operator
print("\n")    
print("Multiplication in Reverse Order")
